using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace calculator
{
    public class Calculator
    {
        public int addInt(int a, int b)
        {
            return a + b;
        }
        public double addDouble(double a, double b)
        {
            return a + b;
        }
        public int minusInt(int a, int b)
        {
            return a - b;
        }
        public double minusDouble(double a, double b)
        {
            return a - b;
        }
        public int multiplyInt(int a, int b)
        {
            return a * b;
        }
        public double multiplyDouble(double a, double b)
        {
            return a * b;
        }
        public int divideInt(int a, int b)
        {
            return a / b;
        }
        public double divideDouble(double a, double b)
        {
            return a / b;
        }
    }
}
